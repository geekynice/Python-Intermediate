mydict = {}
print(mydict)
#result: {}

mydict = dict()
print(mydict)
#result: {}

mydict = {"name": "Nice", "age": 28, "city": "Toronto"}
print(mydict)
#result: {'name': 'Nice', 'age': 28, 'city': 'Toronto'}  

mydict = dict(name="Nice", age=28, city="Toronto")
print(mydict)
#result: {'name': 'Nice', 'age': 28, 'city': 'Toronto'}